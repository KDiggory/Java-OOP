package com.bae.polymorphism;

public class Cakes {
	
	public void hasFlour() {
		boolean flour = true;
		System.out.println("Flour: " + flour);
		
	}
	
	public void serves() {
		int servings = 8;
		System.out.println("Serves: " + servings);
	}
	
	public void icing() {
		boolean icing = false;
		System.out.println("Icing: " + icing);
		
	}
	
	public void flavour() {
		System.out.println("Flavour: Plain");
		
	}

}
